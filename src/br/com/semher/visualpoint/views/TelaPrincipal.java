    // metodo para deixa a tela cheia
    private void TelCheia() {
        if (telacheiasair) {
            itmenuArqTelCh.setForeground(new java.awt.Color(0, 0, 0));
            itmenuArqTelCh.setText("");
            telacheia.add(itmenuArqTelCh);
            telacheia.setUndecorated(true);
            telacheia.setState(MAXIMIZED_BOTH);
            telacheia.setSize(mdtl.modoTelaCheia());
            telacheia.add(PainelRolagem);
            telacheia.setVisible(true);
            setVisible(false);
            telacheiasair = false;
        } else {
            PainelRolagem.getSize();
            itmenuArqTelCh.setText("Tela Cheia");
            itmenuArqTelCh.setForeground(new java.awt.Color(0, 0, 0));
            telacheia.dispose();
            add(PainelRolagem);
            menuTlCheia.add(itmenuArqTelCh);
            setVisible(true);
            telacheiasair = true;
        }
    }

